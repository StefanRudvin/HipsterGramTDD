    public function ToggleFollow(User $user, Request $request)
    {
        $userId = $request->get('user_id');
        $user->toggleFollow($userId);
        $user->save();

        $user->score = $user->followsCount();
        $user->followed = $user->isFollowed($userId);

        return response()->json([
                                    'user' => $user
                                ]);
